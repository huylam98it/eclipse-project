package chuong3;

public class NhanVien {

	private int maNV;
	private int soSP;

	public NhanVien() {
		super();
	}

	public NhanVien(int maNV, int soSP) {
		super();
		this.maNV = maNV;
		this.soSP = soSP;
	}

	public int getMaNV() {
		return maNV;
	}

	public void setMaNV(int maNV) {
		this.maNV = maNV;
	}

	public int getSoSP() {
		return soSP;
	}

	public void setSoSP(int soSP) {
		this.soSP = soSP;
	}

}
